#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	char r;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	r = n % 10 + 48;
	write(1, &r, 1);
}
