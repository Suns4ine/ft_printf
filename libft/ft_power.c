#include "libft.h"

int	ft_power(int x, int y)
{
	int pow;

	pow = 1;
	if (y < 0)
		return (0);
	if (y == 0)
		return (1);
	while (y)
	{
		pow *= x;
		y -= 1;
	}
	return (pow);
}
