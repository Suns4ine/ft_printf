#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	t;

	i = 0;
	t = '\n';
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &t, 1);
}
