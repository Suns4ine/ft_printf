#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	char	r;
	int		rt;

	rt = 0;
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	r = nb % 10 + 48;
	write(fd, &r, 1);
}
